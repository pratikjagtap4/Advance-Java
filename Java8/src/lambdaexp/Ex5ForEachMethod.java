package lambdaexp;

import java.util.ArrayList;
import java.util.Collections;

public class Ex5ForEachMethod {

	public static void main(String[] args) {
		ArrayList<String> movies = new ArrayList();
		
		movies.add("Slam Book");
		movies.add("SitaRamam");
		movies.add("kGF");
		movies.add("Little Things");
		movies.add("Tarzan");
		movies.add("indagi na milegi dobara");
		movies.add("yeh jawani hai deewani");
		movies.add("3idiots");
		
		Collections.sort(movies);
		
		movies.forEach((m) -> {
			m.toLowerCase();
			System.out.println(m);
		});
	}

}
